// Letra A

function somando(n1, n2) {
    return n1 + n2
}

function subtraindo(n1, n2) {
    return n1 - n2
}

function multiplicando(n1, n2) {
    return n1 * n2
}

function dividindo(n1, n2) {
    return n1 / n2
}

// Letra B

const n1 = 12
const n2 = 3

// Letra C e D

const soma = somando(n1, n2)
const subtracao = subtraindo(n1, n2)
const multiplicacao = multiplicando(n1, n2)
const divisao = dividindo(n1, n2)

console.log(`A soma de ${n1} e ${n2} é igual a ${soma}`)
console.log(`A subtração de ${n1} por ${n2} é igual a ${subtracao}`)
console.log(`A multiplicação de ${n1} por ${n2} é igual a ${multiplicacao}`)
console.log(`A divisão de ${n1} e ${n2} é igual a ${divisao}`)